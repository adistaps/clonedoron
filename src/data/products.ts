export const getProductBySlug = (slug: string): Product | undefined => {
  return products.find((p) => p.slug === slug);
};

export const getProductsByCategory = (category: string, subcategory?: string): Product[] => {
  return products.filter((p) => {
    if (subcategory && subcategory !== "all") {
      return p.subcategory === subcategory;
    }
    if (category === "all-assets") return true;
    if (category === "freebies") return p.isFreebie;
    return p.category === category || p.subcategory?.startsWith(category);
  });
};

export const getRelatedProducts = (currentSlug: string, limit: number = 4): Product[] => {
  const current = products.find((p) => p.slug === currentSlug);
  if (!current) return products.slice(0, limit);
  return products
    .filter((p) => p.slug !== currentSlug && (p.category === current.category || p.type === current.type))
    .slice(0, limit);
};
